/* Variáveis

    const (Não pode ser alterado)
    let (Pode ser alterado)
    var (Não é recomendado USAR, é uma forma antiga de criar variáveis)

*/

const abacate = "E aí, ja comprou abacate ?"

abacate = 30

console.log(abacate)

/* Usando const causará erro, nao pode ser alterado o valor, mesmo assim é indicado usar o const, se tiver algum conflito alterar para let  */

let melancia = "E aí, ja comprou melancia ?"

melancia = 30

console.log(melancia)

